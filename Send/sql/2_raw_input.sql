CREATE TABLE raw_input( 
  alien_id int(1), 
  alien_name varchar(8), 
  alien_type varchar(8), 
  alien_color varchar(6), 
  witness_id int(2), 
  witness_name varchar(9), 
  witness_last_name varchar(10), 
  witness_address varchar(61), 
  witness_age int(2), 
  location_id int(3), 
  lat double, 
  lon double, 
  place varchar(13), 
  country varchar(2), 
  region varchar(17), 
  time_of_day varchar(5) 
);